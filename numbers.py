# Ask user for 3 numbers and store them respectively in "num1", "num2" and num3
num1 = int(input("Type the first number : "))

num2 = int(input("Type the second number : "))

num3 = int(input("Type the third number : "))


# Store sum of num1, num2 and num3 in num_sum
num_sum = num1 + num2 + num3

# Display message "Total numbers sum is : " + num_sum
print("Total numbers sum is : ", + num_sum)


# Subtract num2 from num1 store value in sub_num
sub_num = num1 - num2

#  Display message "The first number - the second is : ", + sub_num
print("The first number - the second is : ", + sub_num)


# Multiple num3 with num1 store it in multi_num
multi_num = num3 * num1

# Display message "The third number multiply by the first is : ", + multi_num
print("The third number multiply by the first is : ", + multi_num)


# Divide num_sum by 3 + store it in div_sum
div_sum = num_sum / 3

# Display message "The total sum divided by 3 is : ", + div_sum
print("The total sum divided by 3 is : ", + div_sum)
